#1  What is List? How will you reverse a list? 

"""A list in Python is a collection of elements that is ordered and mutable. 
Lists are one of the built-in data types in Python and are defined by enclosing elements 
in square brackets [], separated by commas. Lists can contain elements of different data types, 
including numbers, strings, other lists, or any other Python objects."""

l = [1,3,4,2,5]
print(l[::-1])
print(l)